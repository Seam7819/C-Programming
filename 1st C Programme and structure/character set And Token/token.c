/*
   In C programming, a token is the smallest unit of meaning in a program. The compiler breaks down the code into tokens for processing. There are six types of tokens:
- Keywords – Reserved words in C (e.g., int, return, if, while).
- Identifiers – Names for variables, functions, arrays (e.g., num, main, sum).
- Constants – Fixed values like numbers or characters (e.g., 10, 'A', 3.14).
- Operators – Symbols that perform operations (e.g., +, -, *, /, =).
- Strings – A sequence of characters enclosed in double quotes (e.g., "Hello").
- Punctuation/Special Symbols – Delimiters like {}, (), ;, [,].
*/